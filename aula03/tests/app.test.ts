describe("operações básicas da matemática", () => {
    it("deve retornar o valor da soma", () => {
        expect(1+1).toEqual(2);
    });
    it("deve retornar o valor da subtração", () => {
        expect(1-1).toEqual(0);
    });
    it("deve retornar o valor da mutiplicação", () => {
        expect(2*2).toEqual(4);
    });
    it("deve retornar o valor da divisão", () => {
        expect(4/2).toEqual(2);
    });
});
